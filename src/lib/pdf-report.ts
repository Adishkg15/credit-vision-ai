function pageCover(doc: jsPDF, r: AssessmentResult, applicant: string) {
  // Hero
  panel(doc, M, 26, PAGE_W - 2 * M, 110);

  // Big score dial
  const cx = M + 50, cy = 80, R = 32;
  setStroke(doc, C.panel2);
  doc.setLineWidth(6);
  doc.circle(cx, cy, R, "S");
  // arc using line segments
  setStroke(doc, C.brand);
  doc.setLineWidth(6);
  const steps = 64;
  const frac = Math.max(0, Math.min(100, r.overallScore)) / 100;
  let prevX = cx, prevY = cy - R;
  for (let i = 1; i <= Math.round(steps * frac); i++) {
    const a = -Math.PI / 2 + (i / steps) * 2 * Math.PI;
    const nx = cx + R * Math.cos(a);
    const ny = cy + R * Math.sin(a);
    doc.line(prevX, prevY, nx, ny);
    prevX = nx; prevY = ny;
  }
  setText(doc, C.text);
  doc.setFont("helvetica", "bold").setFontSize(28);
  doc.text(String(Math.round(r.overallScore)), cx, cy + 2, { align: "center" });
  setText(doc, C.mute);
  doc.setFont("helvetica", "normal").setFontSize(7);
  doc.text("OVERALL SCORE", cx, cy + 9, { align: "center" });

  // Right: applicant + meta
  const rx = M + 100;
  setText(doc, C.mute);
  doc.setFont("helvetica", "normal").setFontSize(8);
  doc.text("APPLICANT", rx, 40);
  setText(doc, C.text);
  doc.setFont("helvetica", "bold").setFontSize(20);
  doc.text(applicant || "Applicant", rx, 50);

  setText(doc, C.mute);
  doc.setFontSize(8);
  doc.text("ASSESSMENT DATE", rx, 62);
  setText(doc, C.text);
  doc.setFont("helvetica", "normal").setFontSize(11);
  doc.text(new Date().toLocaleDateString("en-IN", { day: "2-digit", month: "long", year: "numeric" }), rx, 69);

  setText(doc, C.mute);
  doc.setFontSize(8);
  doc.text("REPORT ID", rx, 79);
  setText(doc, C.text);
  doc.setFont("courier", "normal").setFontSize(10);
  doc.text("CV-" + Date.now().toString(36).toUpperCase(), rx, 86);

  // Status pills
  const pillY = 110;
  drawPill(doc, M + 8, pillY, "Risk: " + r.riskLevel, riskColor(r.riskLevel));
  drawPill(doc, M + 60, pillY, r.eligibilityStatus, eligColor(r.eligibilityStatus));
  drawPill(doc, M + 122, pillY, "Confidence: " + Math.round(r.confidenceScore) + "%", C.brand2);

  // Three KPI cards
  const ky = 146, kw = (PAGE_W - 2 * M - 8) / 3, kh = 30;
  statBlock(doc, M, ky, kw, kh, "Overall Score", String(Math.round(r.overallScore)) + " / 100", C.brand);
  statBlock(doc, M + kw + 4, ky, kw, kh, "Risk Level", r.riskLevel, riskColor(r.riskLevel));
  statBlock(doc, M + 2 * (kw + 4), ky, kw, kh, "Confidence", Math.round(r.confidenceScore) + "%", C.brand2);

  // Category snapshot
  const sy = 186;
  panel(doc, M, sy, PAGE_W - 2 * M, 86);
  setText(doc, C.text);
  doc.setFont("helvetica", "bold").setFontSize(11);
  doc.text("Category Snapshot", M + 6, sy + 8);
  const rowH = 10;
  r.categories.forEach((c, i) => {
    const ry = sy + 16 + i * rowH;
    setText(doc, C.text);
    doc.setFont("helvetica", "normal").setFontSize(9);
    doc.text(c.label, M + 6, ry + 4);
    setText(doc, C.mute);
    doc.setFontSize(7.5);
    doc.text(c.weight + "% weight", M + 70, ry + 4);
    progressBar(doc, M + 95, ry + 2, 70, c.score, C.brand);
    setText(doc, C.text);
    doc.setFont("helvetica", "bold").setFontSize(9);
    doc.text(String(Math.round(c.score)), PAGE_W - M - 6, ry + 4, { align: "right" });
  });
}

function drawPill(doc: jsPDF, x: number, y: number, text: string, color: Rgb) {
  const w = doc.getTextWidth(text) + 10;
  setFill(doc, [color[0], color[1], color[2]]);
  doc.roundedRect(x, y - 5, w, 7.5, 3.5, 3.5, "F");
  setText(doc, [255, 255, 255]);
  doc.setFont("helvetica", "bold").setFontSize(8);
  doc.text(text, x + w / 2, y, { align: "center" });
}

