// Executive PDF Report Generator — CreditVision AI
// Pure jsPDF drawing for a lender-grade, multi-page report.
import { jsPDF } from "jspdf";
import autoTable from "jspdf-autotable";
import type { AssessmentInputs, AssessmentResult, LoanRecommendation } from "./scoring";

type Rgb = [number, number, number];

const C = {
  bg: [15, 19, 33] as Rgb,
  panel: [24, 30, 49] as Rgb,
  panel2: [33, 41, 64] as Rgb,
  border: [54, 65, 92] as Rgb,
  text: [240, 244, 252] as Rgb,
  mute: [156, 169, 197] as Rgb,
  brand: [56, 200, 200] as Rgb,
  brand2: [167, 120, 244] as Rgb,
  success: [60, 195, 130] as Rgb,
  warn: [232, 170, 60] as Rgb,
  danger: [232, 90, 100] as Rgb,
  info: [86, 160, 240] as Rgb,
};

const PAGE_W = 210;
const PAGE_H = 297;
const M = 14; // margin

function setFill(doc: jsPDF, c: Rgb) { doc.setFillColor(c[0], c[1], c[2]); }
function setStroke(doc: jsPDF, c: Rgb) { doc.setDrawColor(c[0], c[1], c[2]); }
function setText(doc: jsPDF, c: Rgb) { doc.setTextColor(c[0], c[1], c[2]); }

function pageFrame(doc: jsPDF, pageNum: number, totalPages: number, applicant: string) {
  // bg
  setFill(doc, C.bg);
  doc.rect(0, 0, PAGE_W, PAGE_H, "F");
  // header bar
  setFill(doc, C.panel);
  doc.rect(0, 0, PAGE_W, 16, "F");
  setFill(doc, C.brand);
  doc.rect(M, 6, 4, 4, "F");
  setText(doc, C.text);
  doc.setFont("helvetica", "bold").setFontSize(11);
  doc.text("CreditVision AI", M + 7, 9.5);
  doc.setFont("helvetica", "normal").setFontSize(8);
  setText(doc, C.mute);
  doc.text("Alternative Credit Assessment", M + 7, 13);
  doc.text(applicant, PAGE_W - M, 9.5, { align: "right" });
  doc.text(new Date().toLocaleDateString("en-IN", { year: "numeric", month: "short", day: "numeric" }), PAGE_W - M, 13, { align: "right" });

  // footer
  setStroke(doc, C.border);
  doc.setLineWidth(0.2);
  doc.line(M, PAGE_H - 12, PAGE_W - M, PAGE_H - 12);
  setText(doc, C.mute);
  doc.setFontSize(7.5);
  doc.text("Confidential — for assessment purposes only. Generated by CreditVision AI.", M, PAGE_H - 7);
  doc.text(`Page ${pageNum} of ${totalPages}`, PAGE_W - M, PAGE_H - 7, { align: "right" });
}

function sectionTitle(doc: jsPDF, y: number, eyebrow: string, title: string) {
  setText(doc, C.brand);
  doc.setFont("helvetica", "bold").setFontSize(8);
  doc.text(eyebrow.toUpperCase(), M, y);
  setText(doc, C.text);
  doc.setFont("helvetica", "bold").setFontSize(16);
  doc.text(title, M, y + 7);
  setStroke(doc, C.border);
  doc.setLineWidth(0.3);
  doc.line(M, y + 10, PAGE_W - M, y + 10);
}

function panel(doc: jsPDF, x: number, y: number, w: number, h: number) {
  setFill(doc, C.panel);
  setStroke(doc, C.border);
  doc.setLineWidth(0.3);
  doc.roundedRect(x, y, w, h, 2, 2, "FD");
}

function statBlock(doc: jsPDF, x: number, y: number, w: number, h: number, label: string, value: string, valueColor: Rgb = C.text) {
  panel(doc, x, y, w, h);
  setText(doc, C.mute);
  doc.setFont("helvetica", "normal").setFontSize(8);
  doc.text(label.toUpperCase(), x + 5, y + 7);
  setText(doc, valueColor);
  doc.setFont("helvetica", "bold").setFontSize(20);
  doc.text(value, x + 5, y + h - 5);
}

function progressBar(doc: jsPDF, x: number, y: number, w: number, pct: number, color: Rgb) {
  setFill(doc, C.panel2);
  doc.roundedRect(x, y, w, 2.5, 1, 1, "F");
  setFill(doc, color);
  const fw = Math.max(0, Math.min(100, pct)) / 100 * w;
  if (fw > 0) doc.roundedRect(x, y, fw, 2.5, 1, 1, "F");
}

function riskColor(level: string): Rgb {
  if (level === "Low") return C.success;
  if (level === "Moderate") return C.info;
  if (level === "Elevated") return C.warn;
  return C.danger;
}
function eligColor(s: string): Rgb {
  if (s === "Approved") return C.success;
  if (s === "Conditional Approval") return C.info;
  if (s === "Manual Review") return C.warn;
  return C.danger;
}

