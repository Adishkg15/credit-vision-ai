function drawRadar(doc: jsPDF, cx: number, cy: number, R: number, labels: string[], values: number[]) {
  const n = labels.length;
  // grid
  setStroke(doc, C.border);
  doc.setLineWidth(0.2);
  for (let g = 1; g <= 4; g++) {
    const r = (R * g) / 4;
    const pts: [number, number][] = [];
    for (let i = 0; i < n; i++) {
      const a = -Math.PI / 2 + (i / n) * 2 * Math.PI;
      pts.push([cx + r * Math.cos(a), cy + r * Math.sin(a)]);
    }
    for (let i = 0; i < n; i++) {
      const [x1, y1] = pts[i];
      const [x2, y2] = pts[(i + 1) % n];
      doc.line(x1, y1, x2, y2);
    }
  }
  // spokes
  for (let i = 0; i < n; i++) {
    const a = -Math.PI / 2 + (i / n) * 2 * Math.PI;
    doc.line(cx, cy, cx + R * Math.cos(a), cy + R * Math.sin(a));
  }
  // data polygon
  const pts: [number, number][] = [];
  for (let i = 0; i < n; i++) {
    const a = -Math.PI / 2 + (i / n) * 2 * Math.PI;
    const r = R * (values[i] / 100);
    pts.push([cx + r * Math.cos(a), cy + r * Math.sin(a)]);
  }
  setStroke(doc, C.brand);
  doc.setLineWidth(0.8);
  setFill(doc, C.brand);
  doc.setGState(new (doc as any).GState({ opacity: 0.25 }));
  // polygon fill via triangles from center
  for (let i = 0; i < n; i++) {
    const [x1, y1] = pts[i];
    const [x2, y2] = pts[(i + 1) % n];
    doc.triangle(cx, cy, x1, y1, x2, y2, "F");
  }
  doc.setGState(new (doc as any).GState({ opacity: 1 }));
  // outline
  for (let i = 0; i < n; i++) {
    const [x1, y1] = pts[i];
    const [x2, y2] = pts[(i + 1) % n];
    doc.line(x1, y1, x2, y2);
  }
  // labels
  setText(doc, C.text);
  doc.setFont("helvetica", "normal").setFontSize(7.5);
  for (let i = 0; i < n; i++) {
    const a = -Math.PI / 2 + (i / n) * 2 * Math.PI;
    const lx = cx + (R + 8) * Math.cos(a);
    const ly = cy + (R + 8) * Math.sin(a) + 1;
    const align = Math.cos(a) > 0.2 ? "left" : Math.cos(a) < -0.2 ? "right" : "center";
    doc.text(labels[i], lx, ly, { align: align as any });
  }
}

