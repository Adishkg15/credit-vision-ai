export function generatePdfReport(opts: {
  applicantName: string;
  inputs: AssessmentInputs;
  result: AssessmentResult;
}): jsPDF {
  const doc = new jsPDF({ unit: "mm", format: "a4" });
  const applicant = opts.applicantName || opts.inputs.name || "Applicant";

  const pages = [
    () => pageCover(doc, opts.result, applicant),
    () => pageSummary(doc, opts.result, applicant),
    () => pageCharts(doc, opts.result),
    () => pageContributors(doc, opts.result),
    () => pageRecommendations(doc, opts.result),
    () => pageRoadmap(doc, opts.result, opts.inputs),
  ];

  pages.forEach((render, idx) => {
    if (idx > 0) doc.addPage();
    pageFrame(doc, idx + 1, pages.length, applicant);
    render();
  });

  return doc;
}

export function downloadPdfReport(opts: Parameters<typeof generatePdfReport>[0]) {
  const doc = generatePdfReport(opts);
  const safe = (opts.applicantName || "applicant").replace(/[^a-z0-9]+/gi, "_");
  doc.save(`CreditVision_Report_${safe}_${Date.now()}.pdf`);
}
