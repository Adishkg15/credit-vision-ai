function deriveRoadmap(r: AssessmentResult, i: AssessmentInputs): { actions: string[]; suggestions: string[] } {
  const actions: string[] = [];
  const suggestions: string[] = [];

  if (i.emergencyFundMonths < 3) actions.push(`Build an emergency fund of at least ${Math.max(3, 6)} months of expenses (currently ${i.emergencyFundMonths} months).`);
  if (i.monthlyIncome > 0 && (i.monthlyIncome - i.monthlyExpenses) / i.monthlyIncome < 0.2) actions.push("Increase monthly surplus to 20%+ of income by reducing discretionary expenses.");
  if (i.avgBankBalance < i.monthlyExpenses) actions.push("Maintain an average bank balance equal to or above one month of expenses.");
  if (i.overdraftEvents > 0) actions.push("Eliminate overdraft events by setting up low-balance alerts and auto-transfers.");
  if (i.failedTransactions > 3) actions.push("Reduce failed transactions — they signal liquidity stress to underwriters.");
  if (i.jobTenureMonths < 12 && !["student", "fresher", "unemployed"].includes(i.employmentType)) actions.push("Sustain current employment for 12+ months to strengthen tenure signal.");
  if (i.certifications < 2) actions.push("Add 1–2 professional certifications to strengthen human-capital score.");

  if (r.missingInformation.includes("Bank transaction history")) suggestions.push("Connect or upload 6 months of bank statements — projected confidence +12–18%.");
  if (r.missingInformation.includes("Verified monthly income")) suggestions.push("Provide salary slips or income proof — projected confidence +10–15%.");
  if (r.missingInformation.includes("Bill payment history")) suggestions.push("Add utility / telecom / rent payment records — projected score +4–7.");
  if (r.missingInformation.includes("Established employment history")) suggestions.push("Once 6+ months of employment data is available, score moves from potential-based to capacity-based.");
  if (i.digitalPaymentUsage < 4) suggestions.push("Increase digital payment usage — projected behavioural score +5–8.");
  if (i.transactionConsistency < 4) suggestions.push("Maintain consistent monthly inflows/outflows — projected score +3–6.");

  if (actions.length === 0) actions.push("Profile is well-balanced — maintain current discipline.");
  if (suggestions.length === 0) suggestions.push("Profile data is comprehensive. Continue regular activity to keep signals fresh.");
  return { actions, suggestions };
}

