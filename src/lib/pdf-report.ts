function drawList(doc: jsPDF, items: string[], x: number, y: number, w: number, color: Rgb, glyph: string, empty: string) {
  if (items.length === 0) {
    setText(doc, C.mute);
    doc.setFont("helvetica", "italic").setFontSize(9);
    doc.text(empty, x, y + 6);
    return;
  }
  let cy = y + 6;
  items.slice(0, 6).forEach(it => {
    setText(doc, color);
    doc.setFont("helvetica", "bold").setFontSize(9);
    doc.text(glyph, x, cy);
    setText(doc, C.text);
    doc.setFont("helvetica", "normal").setFontSize(9);
    const ls = doc.splitTextToSize(it, w - 8);
    doc.text(ls, x + 5, cy);
    cy += ls.length * 4.5 + 2;
  });
}

